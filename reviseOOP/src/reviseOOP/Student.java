package reviseOOP;

/**
 * 
 */
public class Student {
		//instance vars
		private String name;
		private int age;
		
		
		//constructors
		
		
		//methods
		/**
		 * Gets the name
		 * @return
		 */
		public String getName() {
			return name;
		}
		/**
		 * sets name
		 * @param name
		 */
		
		public void setName(String name) {
			this.name = name;
		}
		/**
		 * @return the age
		 */
		public int getAge() {
			return age;
		}
		/**
		 * @param age the age to set
		 */
		public void setAge(int age) {
			this.age = age;
		}

}
