public class LoopExercises {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
			
		int sum, num;
		sum=0;
		num=1;
		sum=num+sum;
		System.out.println("The sum is: "+sum);
		
		while (sum != 11) {
			sum++;
			System.out.println("The sum is: "+sum);
		}
	}

}
